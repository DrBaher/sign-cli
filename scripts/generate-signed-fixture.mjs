// Generates fixtures/web-demo/sample-signed.pdf — a tiny PAdES-signed PDF
// with a visible SVG signature stamped on it. Run by hand whenever the
// fixture needs refreshing; the resulting .pdf is committed to git so the
// hosted demo can link to it without any runtime generation.
//
//   npm run build && node scripts/generate-signed-fixture.mjs
//
// We deliberately bypass the full request/sign service flow — this script
// goes straight to stampImageOnPdf + signPdfLocally so the fixture is
// deterministic and self-contained (no DB, no SQLite writes).

import { mkdirSync, mkdtempSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const fixturesDir = path.join(repoRoot, "fixtures", "web-demo");
const outPath = path.join(fixturesDir, "sample-signed.pdf");

// Isolate signing keys + local store to a temp dir so this script doesn't
// touch the developer's regular SIGN_LOCAL_* directories.
const tmpRoot = mkdtempSync(path.join(os.tmpdir(), "sign-fixture-"));
process.env.SIGN_LOCAL_KEY_DIR = path.join(tmpRoot, "keys");
process.env.SIGN_LOCAL_STORE_DIR = path.join(tmpRoot, "store");
mkdirSync(process.env.SIGN_LOCAL_KEY_DIR, { recursive: true });
mkdirSync(process.env.SIGN_LOCAL_STORE_DIR, { recursive: true });

// Dynamic-import the dist build so the script doesn't need transpilation.
const { stampImageOnPdf } = await import(path.join(repoRoot, "dist", "lib", "pdf-image-stamp.js"));
const { signPdfLocally } = await import(path.join(repoRoot, "dist", "lib", "local-pdf-signer.js"));
const { loadOrCreateSignerKeyPair } = await import(path.join(repoRoot, "dist", "lib", "local-keys.js"));

// A tiny single-page PDF that looks like a contract clip. Plain PostScript
// text operators — no font embedding, no images. About 400 bytes.
const SOURCE_PDF = Buffer.from(`%PDF-1.4
1 0 obj << /Type /Catalog /Pages 2 0 R >> endobj
2 0 obj << /Type /Pages /Kids [3 0 R] /Count 1 >> endobj
3 0 obj << /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792]
  /Resources << /Font << /F1 5 0 R >> >> /Contents 4 0 R >> endobj
4 0 obj << /Length 360 >> stream
BT
/F1 18 Tf 72 740 Td (Mutual NDA \\(sample\\)) Tj
0 -28 Td /F1 11 Tf (This is a fixture PDF generated by sign-cli to demonstrate) Tj
0 -16 Td (how a signed PDF with an embedded signature image is produced.) Tj
0 -16 Td (The signature below was rasterised from SVG and stamped onto the) Tj
0 -16 Td (page before the PAdES envelope was sealed. Tampering with either) Tj
0 -16 Td (the image or the text would break the cryptographic verification.) Tj
0 -56 Td /F1 11 Tf (Signed:) Tj
ET
endstream endobj
5 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica >> endobj
trailer << /Root 1 0 R /Size 6 >>
%%EOF`, "latin1");

// A path-based "handwritten signature" SVG. Deliberately uses no <text>:
// @resvg/resvg-wasm ships zero bundled fonts, so any text-based signature
// would render as an empty rectangle (silently). Paths render identically
// regardless of font stack, which is what we want for a hermetic fixture.
// Reads as a capital "A" + flowing scribble + flourish + underline.
const SIGNATURE_SVG = Buffer.from(`<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 240 70" width="240" height="70">
  <path d="M 18 52 Q 30 8, 42 52 M 24 36 L 38 36"
        stroke="#0a3b91" fill="none" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round"/>
  <path d="M 44 48 C 60 30, 80 56, 100 42 C 120 30, 140 52, 160 40 C 180 30, 200 48, 218 38"
        stroke="#0a3b91" fill="none" stroke-width="2" stroke-linecap="round"/>
  <path d="M 215 38 Q 232 30, 222 55 Q 210 58, 218 42"
        stroke="#0a3b91" fill="none" stroke-width="1.5" stroke-linecap="round"/>
  <line x1="8" y1="62" x2="232" y2="62" stroke="#0a3b91" stroke-width="1.2"/>
</svg>`, "utf8");

console.log(`[fixture] stamping SVG signature onto source PDF`);
const stamped = await stampImageOnPdf(
  SOURCE_PDF,
  { kind: "buffer", data: SIGNATURE_SVG, mime: "image/svg+xml" },
  { page: 1, x: 120, y: 600, width: 180, height: 50 },
);

console.log(`[fixture] applying PAdES envelope (single signer: alice@example.com)`);
const keyPair = loadOrCreateSignerKeyPair({ email: "alice@example.com", name: "Alice Anderson" });
const signed = signPdfLocally(stamped, {
  signerKeyPair: keyPair,
  signerLabel: "alice@example.com",
  signingTime: new Date("2026-01-15T14:30:00Z"),  // deterministic timestamp
});

mkdirSync(fixturesDir, { recursive: true });
writeFileSync(outPath, signed.signedPdf);

// Clean up the temp keys/store dir so the developer's env stays clean.
rmSync(tmpRoot, { recursive: true, force: true });

console.log(`[fixture] wrote ${outPath} (${signed.signedPdf.length} bytes)`);
